local basefunc = require "Game/Common/basefunc"

BossTalk = basefunc.class()
local M = BossTalk
M.name = "BossTalk"

function M.Create(data)
	return M.New(data)
end

function M:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function M:MakeLister()
    self.lister = {}
end

function M:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function M:Exit()
	if self.Timer then
		self.Timer:Stop()
	end
	self:RemoveListener()
	Destroy(self.gameObject)
end

function M:OnDestroy()
	self:Exit()
end

function M:MyClose()
	self:Exit()
end

function M:Ctor(data)
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/LayerLv50").transform
	local obj = NewObject(M.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	self.data = data
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function M:InitUI()
	local end_time = 0
	self.Timer = Timer.New(function()	
		self.transform.position = CSModel.Get3DToUIPoint(self.data.pos)
		end_time = end_time + 0.016
		if end_time > 2.6 then
			self:Exit()
		end
	end,0.016,-1)
	self.Timer:Start()
	self:MyRefresh()
end

