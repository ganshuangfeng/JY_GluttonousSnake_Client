        end
    else
        for idx = #tbl, 1, -1 do
            array[#array + 1] = ""..tbl[idx]
        end
    end
    return array
end

--取整
MathExtend.Integer = function (num,decimal)
    decimal = decimal or 0.5

    local integer, _decimal = math.modf(num)

    if _decimal >= decimal then
        return integer + 1
    else
        return integer
    end
end

