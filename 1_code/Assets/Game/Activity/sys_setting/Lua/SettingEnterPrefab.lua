local basefunc = require "Game/Common/basefunc"

SettingEnterPrefab = basefunc.class()
local C = SettingEnterPrefab
C.name = "SettingEnterPrefab"

function C.Create(parent)
	return C.New(parent)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:Exit()
	self:RemoveListener()
	Destroy(self.gameObject)
end

function C:OnDestroy()
	self:Exit()
end

function C:MyClose()
	self:Exit()
end

function C:Ctor(parent)
	ExtPanel.ExtMsg(self)
	local obj
	if MainModel.myLocation == GameSceneManager.EnterFirstScene then
		obj = NewObject("SettingEnterPrefab", parent)
	else
		obj = NewObject("SettingEnterPrefab2", parent)
	end

	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.enter_btn.onClick:AddListener(function ()
		--ExtendSoundManager.PlaySound(audio_config.game.com_but_cancel.audio_name)
		SettingPanel.Show()
	end)

	self:MyRefresh()
end

function C:MyRefresh()
end
